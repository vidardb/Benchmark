-- Create PostgreSQL benchmark table --

DROP FOREIGN TABLE IF EXISTS LINEITEM;
DROP TABLE IF EXISTS LINEITEM;

CREATE TABLE LINEITEM (
    L_ORDERKEY       INTEGER,
    L_PARTKEY        INTEGER,
    L_SUPPKEY        INTEGER,
    L_LINENUMBER     INTEGER,
    L_QUANTITY       FLOAT,
    L_EXTENDEDPRICE  FLOAT,
    L_DISCOUNT       FLOAT,
    L_TAX            FLOAT,
    L_RETURNFLAG     CHAR(1),
    L_LINESTATUS     CHAR(1),
    L_SHIPDATE       DATE,
    L_COMMITDATE     DATE,
    L_RECEIPTDATE    DATE,
    L_SHIPINSTRUCT   TEXT,
    L_SHIPMODE       TEXT,
    L_COMMENT        TEXT,
    primary key(L_ORDERKEY, L_LINENUMBER)
);

